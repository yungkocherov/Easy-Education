        <p><b>Непараметрические тесты</b> не делают предположений о форме распределения. Вместо того чтобы работать с сырыми значениями, они работают с <b>рангами</b> — позициями элементов в отсортированной последовательности. Это делает их устойчивыми к выбросам и работающими для любых распределений.</p>
