        <p>В ML это происходит, когда модель на этапе обучения <b>видит</b> что-то, чего она не должна знать в момент предсказания: будущие значения, информацию из теста, признаки, которые в реальности недоступны. Result: метрики отличные, в проде катастрофа.</p>
