      <p>Для A/B тестов на практике:</p>
      <ul>
        <li><b>Двусторонний</b> ($H_1$: $p_A \\neq p_B$): стандартный выбор. Ловит эффект в обе стороны. p-value = $2 \\cdot (1 - \\Phi(|z|))$. Критическое |z| = 1.96 при α=0.05.</li>
